// tr — komponentlərdə tək sətrin dəqiq (exact-match) tərcüməsi.
// T (statik UI) + MENU_T (menyu) birləşir; translateStatic YALNIZ T-ni işlədir
// → menyu tərcümələri gövdə mətnində substring mangling yaratmır.
const TR_MAP: Map<string, readonly [string, string]> = new Map(
  [...T, ...MENU_T].map(([az, ru, en]) => [az, [ru, en] as const])
);
export function tr(az: string, locale: Locale): string {
  if (locale === 'az' || !az) return az;
  const hit = TR_MAP.get(az);
  if (!hit) return az;
  return locale === 'ru' ? hit[0] : hit[1];
}
